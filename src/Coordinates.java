public class Coordinates {

        /**
         * The x of node
         */
        private int x;

        /**
         * The y of node
         */
        private int y;

        // constructor
        public Coordinates(int x, int y) {
            this.x = x;
            this.y = y;
        }

        // getters and setters
        public int getX() {
            return x;
        }

        public int getY() {
            return y;
        }

        public void setX(int x) {
            this.x = x;
        }

        public void setY(int y) {
            this.y = y;
        }

}

